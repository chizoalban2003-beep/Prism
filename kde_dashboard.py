# ANSI colour codes
_GREEN  = "\033[92m"
_YELLOW = "\033[93m"
_RED    = "\033[91m"
_CYAN   = "\033[96m"
_BOLD   = "\033[1m"
_RESET  = "\033[0m"

_RISK_COLOURS = {
    "low":      _GREEN,
    "moderate": _YELLOW,
    "high":     _RED,
    "critical": _RED + _BOLD,
}


class TerminalDashboard:
    """
    Text-based dashboard for terminal display.
    Uses only print() — no curses, no rich, no blessed.
    """

    def __init__(self, use_colour: bool = True) -> None:
        self._colour = use_colour and _terminal_supports_colour()

    def _c(self, text: str, code: str) -> str:
        if self._colour:
            return f"{code}{text}{_RESET}"
        return text

    # ── Morning Brief ─────────────────────────────────────────────────────

    def morning_brief(self, plan, wearable: str = "") -> None:
        """Print formatted morning briefing to stdout."""
        print()
        print(self._c("═" * 50, _CYAN))
        print(self._c("  🌅  MORNING BRIEFING", _BOLD + _CYAN))
        print(self._c("═" * 50, _CYAN))
        if wearable:
            print(f"  Wearable  : {wearable}")
        if hasattr(plan, "primary_focus"):
            print(f"  Focus     : {self._c(plan.primary_focus, _BOLD)}")
            print(f"  Activation: {plan.activation:.0%}")
            print(f"  Fulcrum   : {plan.fulcrum:.3f}")
            if plan.tasks:
                print(f"\n  {self._c('Tasks:', _CYAN)}")
                for t in plan.tasks[:5]:
                    print(f"    {t.time_slot}  {t.title} ({t.duration_min}min)")
            if plan.warnings:
                print(f"\n  {self._c('⚠ Warnings:', _YELLOW)}")
                for w in plan.warnings:
                    print(f"    {self._c(w, _YELLOW)}")
        print(self._c("═" * 50, _CYAN))
        print()

    # ── Match Prediction ──────────────────────────────────────────────────

    def match_prediction(self, pred) -> None:
        """Print formatted match prediction with ASCII probability bars."""
        print()
        print(self._c("═" * 50, _CYAN))
        print(self._c(f"  ⚽  {pred.subject}", _BOLD + _CYAN))
        print(self._c("═" * 50, _CYAN))
        print(f"  Prediction : {self._c(pred.prediction, _BOLD)}")
        print(f"  Confidence : {pred.confidence:.0%}")
        print()
        dist = pred.distribution
        for outcome, prob in dist.items():
            bar_len = int(prob * 30)
            bar     = "█" * bar_len + "░" * (30 - bar_len)
            label   = outcome.replace("_", " ").title()
            colour  = _GREEN if prob == max(dist.values()) else _RESET
            print(f"  {label:<15} {self._c(bar, colour)}  {prob:.1%}")
        print()
        print(f"  Margin est.: {pred.predicted_margin:+.1f}")
        print(self._c("═" * 50, _CYAN))
        print()

    # ── Squad Risk ────────────────────────────────────────────────────────

    def squad_risk(self, risks: list) -> None:
        """Print squad risk table with coloured risk levels."""
        print()
        print(self._c("═" * 60, _CYAN))
        print(self._c("  🏥  SQUAD RISK OVERVIEW", _BOLD + _CYAN))
        print(self._c("═" * 60, _CYAN))
        print(f"  {'Player':<22} {'Risk':<12} {'Days':<8} Recommendation")
        print("  " + "─" * 55)
        for rp in risks:
            colour = _RISK_COLOURS.get(rp.risk_level, _RESET)
            rec    = rp.recommendations[0] if rp.recommendations else "Monitor"
            level  = self._c(f"{rp.risk_level:<12}", colour)
            print(f"  {rp.athlete_name:<22} {level} {rp.days_to_risk:<8d} {rec}")
        print(self._c("═" * 60, _CYAN))
        print()


